%	h = holidays;
%	h = holidays(d1, d2, altholidays);
%
% Optional Inputs:
%	d1 - First date of a specified date range. Either a date string or a serial date number. If d1
%		is specified, then d2 is necessary
%	d2 - Last date of a specified date range. Either a date string or a serial date number. Required
